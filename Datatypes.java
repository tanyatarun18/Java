
public class Datatypes
{
    public static void main(String[] args)
    {
        byte a = 3; // Size - 1 bytes [can store from -128 to 127]
        System.out.println(a);
        int b = 4;  // 4 bytes
        float c = 34.4f;  // 4 bytes
        long d =  79;  // 8 bytes
        short e = 23; //2 bytes
        char sa = 'a'; // 2 bytes
        boolean var = true; // 1 byte
        double da = 4432.9864987; // 8 bytes
    }
}
