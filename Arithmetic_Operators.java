public class Arithmetic_Operators
{
    public static void main(String[] args) {
        int a = 32;
        int b = 5;
        System.out.println("Sum = " + (a+b));
        System.out.println("Sub = " + (a-b));
        System.out.println("Mul = " + (a*b));
        System.out.println("Div = " + (a/b));
        System.out.println("Mod = " + (a%b));
    }
}
