public class AssignmentOperator
{
    public static void main(String[] args) {
        int a = 35;
        int b = 57;
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
        System.out.println(a = b);
    }
}
